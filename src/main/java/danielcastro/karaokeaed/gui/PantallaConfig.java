    protected EntityManager updatePersistence() {
        Map<String, String> persistenceMap = new HashMap<>();
        persistenceMap.put("javax.persistence.jdbc.driver", "org.mariadb.jdbc.Driver");
        persistenceMap.put("hibernate.dialect", "org.hibernate.dialect.MariaDBDialect");
        persistenceMap.put("javax.persistence.jdbc.user", fieldUsuario.getText());
        persistenceMap.put("javax.persistence.jdbc.password", fieldContrasegna.getText());
        EntityManagerFactory managerFactory = Persistence.createEntityManagerFactory("CRM", persistenceMap);
        EntityManager manager = managerFactory.createEntityManager();
        return manager;
    }

